<script setup lang="ts">
import { ref } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '@/stores/auth';

const userForm = ref({
    email: '',
    password: ''
})

const errorForm = ref('')
const router = useRouter()
const authStore = useAuthStore()

const onSubmit = async() => {

    // console.log(userForm.value)
    const { email, password } = userForm.value
    userForm.value.email = ''
    userForm.value.password = ''
    errorForm.value = ''

    const { ok, message } = await authStore.loginWithEmailPassword(email, password)
    
    if( ok ) {
        errorForm.value = ''
        router.push({ name: 'home' })
    } else {
        errorForm.value = message || ''
    }
    
}

const loginWithGoogle = async() => {
    const { ok } = await authStore.createUserGoogle()

    if(ok) {
        router.push({ name: 'home' })
    }

    return
}

const loginWithGithub = async() => {
    const { ok } = await authStore.createUserGithub()

    if(ok) {
        router.push({ name: 'home' })
    }

    return
}


